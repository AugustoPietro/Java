package aula09;


public class Exemplo01 {

    public static void main(String[] args) {
        
        int num = 1;
        int n = 1;
        
        while (num < 10) {
            System.out.println(num);
            num++;
        }
        
        System.out.println("------------------");
        
        do {
            System.out.println(n);
            n++;
        } while (n < 10);
    }
    
}
